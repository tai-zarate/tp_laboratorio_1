#include <stdio.h>
#include <stdlib.h>
#include <string.h>
/**
 * Pide el ingreso de un entero
 *
 * @param Mensaje para el usuario
 * @return Entero ingresado
 */
int GetInt (char[]);

/**
 * Pide el ingreso de un flotante
 *
 * @param Mensaje para el usuario
 * @return Flotante ingresado
 */
float GetFloat (char[]);

/**
 * Pide el ingreso de un caracter
 *
 * @param Mensaje para el usuario
 * @return Caracter ingresado
 */
char GetChar (char[]);

/**
 * Pide el ingreso de un array de caracteres
 *
 * @param Mensaje para el usuario
 * @param Array donde se almacenara el array de caracteres ingresado
 */
void GetString(char *, char *);
