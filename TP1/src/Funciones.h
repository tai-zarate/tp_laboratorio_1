#ifndef FUNCIONES_H_
#define FUNCIONES_H_

/**
 * @fn int SumarNumeros(int, int)
 * @brief Suma los dos números ingresados
 *
 * @param Primer número ingresado
 * @param Segundo número ingresado
 * @return El resultado de la suma
 */
int SumarNumeros (int, int);

/**
 * @fn int RestarNumeros(int, int)
 * @brief Resta los dos número ingresados
 *
 * @param Primer número ingresado
 * @param Segundo número ingresado
 * @return El resultado de la resta
 */
int RestarNumeros (int, int);

/**
 * @fn float DivisionNumeros(int, int)
 * @brief Divide el primer número ingresado por el segundo
 *
 * @param Primer número ingresado
 * @param Segundo número ingresado
 * @return El resultado de la división
 */
float DivisionNumeros (int, int);

/**
 * @fn int MultiplicarNumeros(int, int)
 * @brief Multiplica los números ingresados
 *
 * @param Primer número ingresado
 * @param Segundo número ingresado
 * @return El resultado de la multiplicación
 */
int MultiplicarNumeros (int, int);

/**
 * @fn int FactorialNumero(int)
 * @brief Calcula el factorial de un número ingresado
 *
 * @param El número ingresado al que se le desea calcular el factorial
 * @return El resultado  del factorial de un número ingresado
 */
int FactorialNumero (int);

#endif /* FUNCIONES_H_ */
